// Helper functions
export const getCourseById = (id: string) => mockCourses.find(c => c.id === id);
export const getUserById = (id: string) => mockUsers.find(u => u.id === id);
export const getEnrolledCourses = (studentId: string) => {
  const enrollments = mockEnrollments.filter(e => e.studentId === studentId);
  return enrollments.map(e => ({
    ...getCourseById(e.courseId)!,
    enrollment: e,
  }));
};
export const getCoursesByInstructor = (instructorId: string) => 
  mockCourses.filter(c => c.instructorId === instructorId);
